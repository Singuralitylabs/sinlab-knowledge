/**
 * Migrate the legacy `claude-code-website` (Next.js + JSX) feature pages into
 * the Sinlab Skills `content/themes/03-ai-driven-development/` tree.
 *
 * Usage:
 *   bun scripts/migrate/import-claude-code-website.ts
 *
 * Strategy notes
 * --------------
 * The source pages are React Server Components mixing JSX, inline data
 * structures and helper components (`CodeBlock`, `TipBox`, etc.). Building a
 * faithful AST -> Markdown converter is out of scope for Phase 1, so this
 * script takes a deliberately pragmatic approach:
 *
 *   1. The lesson title and description are taken from a hand-curated table
 *      (more reliable than scraping the JSX `<h1>` block).
 *   2. The body is generated by a *best-effort* JSX text extractor that pulls
 *      out `<h2>`, `<h3>`, `<p>` text and `CodeBlock` contents and emits
 *      readable Markdown. Anything we cannot map cleanly is dropped.
 *   3. For the highly structured `commands/page.tsx` page, the `commandCategories`
 *      TS literal is parsed via a small recognizer and rendered as Markdown
 *      tables.
 *   4. Each generated file ends with a "原典" link back to the legacy site so
 *      readers can see the original UI while we polish the Markdown.
 *
 * The Phase 1 acceptance bar is "9 valid Markdown files that pass the zod
 * lesson schema". Content polish happens in a follow-up phase.
 */

