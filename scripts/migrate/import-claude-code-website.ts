function buildBody(plan: LessonPlan, sourcePath: string | null): BuildResult {
  if (plan.source === null) {
    // Phase 1 newly authored lesson.
    return { body: buildAuthoredGettingStarted(), mode: "authored" };
  }
  if (!sourcePath || !existsSync(sourcePath)) {
    return { body: buildPlaceholderBody(plan), mode: "placeholder" };
  }
  const source = readFileSync(sourcePath, "utf-8");

  if (plan.source === "commands") {
    const categories = parseCommandCategories(source);
    if (categories.length > 0) {
      const intro =
        "Claude Code の対話モードや CLI から呼び出せるコマンドを、カテゴリ別にまとめたチートシートです。";
      const body = `${intro}\n${renderCommandCategoriesMd(categories)}\n`;
      return { body, mode: "structured" };
    }
    // Fall through to generic extraction if parsing failed.
  }

  const items = extractItems(source);
  if (items.length === 0) {
    return { body: buildPlaceholderBody(plan), mode: "placeholder" };
  }
  const md = itemsToMarkdown(items, plan.title);
  return { body: `${md}\n`, mode: "extracted" };
}

