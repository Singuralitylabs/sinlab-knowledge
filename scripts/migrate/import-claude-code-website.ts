// JSX -> Markdown extraction (best effort)
// ---------------------------------------------------------------------------

/**
 * Strip JSX expression containers, JSX entities and HTML tags from a snippet
 * to produce plain text. Whitespace is collapsed.
 */
function jsxToPlainText(snippet: string): string {
  let out = snippet;

  // Drop `{" "}`, `{","}` and similar string-only expressions.
  out = out.replace(/\{"\s*([^"]*)\s*"\}/g, "$1");
  out = out.replace(/\{`\s*([^`]*)\s*`\}/g, "$1");
  // Replace `{...}` interpolations we can't read with empty string.
  out = out.replace(/\{[^{}]*\}/g, "");
  // Strip remaining JSX tags.
  out = out.replace(/<[^>]+>/g, "");
  // Decode common entities that the source uses.
  out = out
    .replace(/&quot;/g, '"')
    .replace(/&amp;/g, "&")
    .replace(/&#10003;/g, "✓")
    .replace(/&#8595;/g, "↓")
    .replace(/&apos;/g, "'");
  // Normalise whitespace.
  out = out.replace(/\s+/g, " ").trim();
  return out;
}

interface ExtractedItem {
  kind: "heading" | "paragraph" | "code";
  level?: 2 | 3;
  text?: string;
  codeLabel?: string;
  code?: string;
}

/**
 * Walk the source TSX in document order and pull out a flat list of headings,
 * paragraphs and code blocks. We avoid an actual JSX parser; the source uses
 * very stylised formatting that regex can handle for our limited needs.
 */
function extractItems(source: string): ExtractedItem[] {
  const items: ExtractedItem[] = [];

  // Pattern matches headings, paragraphs (single-tag, possibly multi-line) and
  // <CodeBlock label="...">{`...`}</CodeBlock> blocks. We iterate manually to
  // preserve document order.
  const tokenPattern =
    /<h(2|3)\b[^>]*>([\s\S]*?)<\/h\1>|<p\b[^>]*>([\s\S]*?)<\/p>|<CodeBlock\s+label=\{?"([^"]*)"\}?\s*>\s*\{`([\s\S]*?)`\}\s*<\/CodeBlock>/g;

  // biome-ignore lint/suspicious/noAssignInExpressions: classic exec pattern
  for (let m: RegExpExecArray | null; (m = tokenPattern.exec(source)) !== null; ) {
    if (m[1]) {
      const level = (m[1] === "2" ? 2 : 3) as 2 | 3;
      const text = jsxToPlainText(m[2]);
      if (text) items.push({ kind: "heading", level, text });
    } else if (m[3] !== undefined) {
      const text = jsxToPlainText(m[3]);
      if (text) items.push({ kind: "paragraph", text });
    } else if (m[4] !== undefined && m[5] !== undefined) {
      items.push({ kind: "code", codeLabel: m[4], code: m[5] });
    }
  }

  return items;
}

/**
 * Render extracted items as Markdown. Drops the very first H2 if it duplicates
 * the lesson title (common in the source pages where the hero <h1> repeats as
 * an in-page summary heading).
 */
function itemsToMarkdown(items: ExtractedItem[], lessonTitle: string): string {
  const lines: string[] = [];
  let lastWasHeading = false;
  let firstHeadingSeen = false;

  for (const item of items) {
    if (item.kind === "heading" && item.level && item.text) {
      if (!firstHeadingSeen && item.level === 2 && item.text === lessonTitle) {
        firstHeadingSeen = true;
        continue;
      }
      firstHeadingSeen = true;
      const hashes = item.level === 2 ? "##" : "###";
      if (lines.length > 0) lines.push("");
      lines.push(`${hashes} ${item.text}`);
      lastWasHeading = true;
    } else if (item.kind === "paragraph" && item.text) {
      if (lines.length > 0 && !lastWasHeading) lines.push("");
      else if (lastWasHeading) lines.push("");
      lines.push(item.text);
      lastWasHeading = false;
    } else if (item.kind === "code" && item.code !== undefined) {
      if (lines.length > 0) lines.push("");
      const lang = guessFenceLang(item.codeLabel ?? "", item.code);
      lines.push(`\`\`\`${lang}`);
      lines.push(item.code.replace(/\n$/, ""));
      lines.push("```");
      lastWasHeading = false;
    }
  }

  return lines.join("\n");
}

function guessFenceLang(label: string, code: string): string {
  const lower = label.toLowerCase();
  if (lower.includes("json")) return "json";
  if (lower.includes("yaml") || lower.includes("yml")) return "yaml";
  if (lower.includes("typescript") || lower.includes("ts")) return "ts";
  if (lower.includes("javascript") || lower.includes("js")) return "js";
  if (/\b(bash|shell|sh|terminal|cli)\b/.test(lower)) return "bash";
  // Fallbacks based on code content.
  if (/^\s*\{/.test(code)) return "json";
  if (/^\s*\$\s/m.test(code) || /^npm |^bun |^claude /m.test(code.trim())) return "bash";
  return "text";
}

// ---------------------------------------------------------------------------
// Special handler: commands page (structured data)
// ---------------------------------------------------------------------------

interface CommandRow {
  name: string;
  description: string;
  usage: string;
  tip?: string;
}

interface CommandCategoryRow {
  title: string;
  description: string;
  commands: CommandRow[];
}

/**
 * Parse the `commandCategories: CommandCategory[] = [...]` literal from the
 * commands page source. The source uses a stable, hand-maintained shape so we
 * can extract each `{ name, description, usage, tip? }` record with regex.
 */
function parseCommandCategories(source: string): CommandCategoryRow[] {
  const literalStart = source.indexOf("const commandCategories");
  if (literalStart < 0) return [];
  const eqPos = source.indexOf("=", literalStart);
  if (eqPos < 0) return [];
  const arrayStart = source.indexOf("[", eqPos);
  if (arrayStart < 0) return [];
  // Walk until we balance brackets to find the end of the literal.
  let depth = 0;
  let end = -1;
  for (let i = arrayStart; i < source.length; i++) {
    const c = source[i];
    if (c === "[") depth++;
    else if (c === "]") {
      depth--;
      if (depth === 0) {
        end = i;
        break;
      }
    }
  }
  if (end < 0) return [];
  const literal = source.slice(arrayStart, end + 1);

  const categories: CommandCategoryRow[] = [];
  // Find each top-level category object.
  const categoryPattern =
    /title:\s*"([^"]+)",[\s\S]*?description:\s*"([^"]+)",[\s\S]*?commands:\s*\[([\s\S]*?)\],?\s*\}/g;
  // biome-ignore lint/suspicious/noAssignInExpressions: classic exec pattern
  for (let m: RegExpExecArray | null; (m = categoryPattern.exec(literal)) !== null; ) {
    const [, title, description, commandsBlock] = m;
    const commands: CommandRow[] = [];
    const cmdPattern =
      /\{\s*name:\s*"([^"]+)",\s*description:\s*"([^"]+)",\s*usage:\s*"([^"]+)"(?:,\s*tip:\s*"([^"]+)")?\s*,?\s*\}/g;
    // biome-ignore lint/suspicious/noAssignInExpressions: classic exec pattern
    for (let cm: RegExpExecArray | null; (cm = cmdPattern.exec(commandsBlock)) !== null; ) {
      commands.push({
        name: cm[1],
        description: cm[2],
        usage: cm[3],
        tip: cm[4],
      });
    }
    categories.push({ title, description, commands });
  }
  return categories;
}

function escapeTableCell(value: string): string {
  return value.replace(/\|/g, "\\|").replace(/\n/g, " ");
}

function renderCommandCategoriesMd(categories: CommandCategoryRow[]): string {
  const lines: string[] = [];
  for (const cat of categories) {
    lines.push("");
    lines.push(`## ${cat.title}`);
    lines.push("");
    lines.push(cat.description);
    lines.push("");
    lines.push("| コマンド | 説明 | 使い方 | Tip |");
    lines.push("|---|---|---|---|");
    for (const cmd of cat.commands) {
      const tip = cmd.tip ? escapeTableCell(cmd.tip) : "";
      lines.push(
        `| \`${escapeTableCell(cmd.name)}\` | ${escapeTableCell(cmd.description)} | \`${escapeTableCell(cmd.usage)}\` | ${tip} |`,
      );
    }
  }
  return lines.join("\n");
}

