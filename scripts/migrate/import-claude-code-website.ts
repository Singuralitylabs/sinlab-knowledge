interface RunStats {
  written: { file: string; mode: BuildResult["mode"] }[];
  warnings: string[];
  errors: string[];
}

function buildLegacyFooter(plan: LessonPlan): string {
  if (!plan.legacyPath) return "";
  return ["---", "", `**原典**: \`claude-code-website/${plan.legacyPath}/page.tsx\``, ""].join(
    "\n",
  );
}

function migrateLesson(plan: LessonPlan, stats: RunStats): void {
  const sourcePath = plan.source ? path.join(LEGACY_FEATURES_DIR, plan.source, "page.tsx") : null;
  const built = buildBody(plan, sourcePath);
  const footer = buildLegacyFooter(plan);
  const fullBody = footer ? `${built.body}\n${footer}` : built.body;

  const fm: LessonFrontmatter = {
    title: plan.title,
    description: plan.description,
    order: plan.order,
    type: plan.type,
    difficulty: "intermediate",
    tags: ["claude-code", "ai-coding", ...plan.extraTags],
    estimatedMinutes: plan.estimatedMinutes,
    status: "published",
    relatedLessons: [],
    authors: [],
  };

  const output = `${renderFrontmatter(fm)}${fullBody}`;
  const targetPath = path.join(LESSONS_OUT, plan.targetFile);

  try {
    parseLessonSource(output, targetPath);
  } catch (err) {
    stats.errors.push(`schema validation failed for ${targetPath}: ${(err as Error).message}`);
    return;
  }

  ensureDir(path.dirname(targetPath));
  writeFileSync(targetPath, output, "utf-8");
  stats.written.push({ file: path.relative(PROJECT_ROOT, targetPath), mode: built.mode });
}

function main(): void {
  const stats: RunStats = { written: [], warnings: [], errors: [] };

  ensureThemeMeta();
  ensureModuleStubs();
  ensureDir(LESSONS_OUT);

  for (const plan of PLAN) {
    if (plan.source) {
      const sourcePath = path.join(LEGACY_FEATURES_DIR, plan.source, "page.tsx");
      if (!existsSync(sourcePath)) {
        stats.warnings.push(
          `source missing: ${sourcePath} (will use placeholder for ${plan.targetFile})`,
        );
      }
    }
    migrateLesson(plan, stats);
  }

  console.log("\nMigration complete.");
  console.log(`  written:  ${stats.written.length} files`);
  console.log(`  warnings: ${stats.warnings.length}`);
  console.log(`  errors:   ${stats.errors.length}`);

  console.log("\nFiles:");
  for (const w of stats.written) {
    console.log(`  - [${w.mode.padEnd(11)}] ${w.file}`);
  }

  if (stats.warnings.length > 0) {
    console.log("\nWarnings:");
    for (const w of stats.warnings) console.log(`  - ${w}`);
  }
  if (stats.errors.length > 0) {
    console.log("\nErrors:");
    for (const e of stats.errors) console.log(`  - ${e}`);
    process.exitCode = 1;
  }

  // Reference legacy site base so the constant isn't reported as unused by the
  // bundler when tree-shaken (used by future enhancements to construct full URLs).
  void LEGACY_SITE_BASE;
}

main();
